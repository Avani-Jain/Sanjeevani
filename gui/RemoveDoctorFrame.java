    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int ans = JOptionPane.showConfirmDialog(null, "Are you sure?", "REMOVE", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if(ans == JOptionPane.NO_OPTION){
            JOptionPane.showMessageDialog(null, "Record not deleted", "Status", JOptionPane.INFORMATION_MESSAGE);
            txtdocName.setText("");
            jcbDocId.setSelectedIndex(0);
            return;
        }
        else{      
            try{
                boolean result = DoctorDao.removeDoc(docId);
            if(result){
                JOptionPane.showMessageDialog(null, "Record deleted successfully!", "Sucess", JOptionPane.INFORMATION_MESSAGE);
                jcbDocId.removeItem(docId);
                if(jcbDocId.getItemCount()==0){
                    btnRemove.setEnabled(false);
                    JOptionPane.showMessageDialog(null, "All doctors removed");
                    ManageDoctorFrame op = new ManageDoctorFrame();
                    op.setVisible(true);
                    this.dispose();
                    return;
                }     
            }
            else{
        JOptionPane.showMessageDialog(null,"Record not deleted!","Failure!",JOptionPane.ERROR_MESSAGE);
      }}
      catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void jcbDocIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbDocIdActionPerformed
        if(jcbDocId.getSelectedIndex()!=-1){
            docId = jcbDocId.getSelectedItem().toString();
            try {
                txtdocName.setText(DoctorDao.getDocName(jcbDocId.getSelectedItem().toString()));
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            }
            btnRemove.setEnabled(true);
        }
        
        
    }//GEN-LAST:event_jcbDocIdActionPerformed

