    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
        int ans = JOptionPane.showConfirmDialog(null, "Are you sure?", "REMOVE", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
        if(ans == JOptionPane.NO_OPTION){
            JOptionPane.showMessageDialog(null, "Record not deleted", "Status", JOptionPane.INFORMATION_MESSAGE);
            txtEmpName.setText("");
            txtEmpSal.setText("");
            jcbDepartment.setSelectedIndex(0);
            jcbEmpId.setSelectedIndex(0);
            return;
            
        }
        else{
            System.out.println("Else entered");
            if(jcbDepartment.getSelectedItem().toString().equalsIgnoreCase("admin")){
                JOptionPane.showMessageDialog(null, "Cannot be deleted!", "Error", JOptionPane.ERROR_MESSAGE);
                System.out.println("Admin selected");
                return;   
            }
//            boolean isValid = validateInputs();
//            if(!isValid){
//                JOptionPane.showMessageDialog(null, "Please input all data!", "Error", JOptionPane.ERROR_MESSAGE);
//                return;
//            }
            
//            System.out.println("validate inputs passed!");
            try{
                double eSal = Double.parseDouble(txtEmpSal.getText());
                String empName = txtEmpName.getText();
            
            EmpPojo emp = new EmpPojo();
            emp.setEmpId(jcbEmpId.getSelectedItem().toString());
            emp.setEmpName(empName);
            emp.setEmpSal(eSal);
            emp.setEmpDepartment(jcbDepartment.getSelectedItem().toString());
            boolean result = EmpDao.removeEmp(emp);
            
            if(result){
                JOptionPane.showMessageDialog(null, "Record deleted successfully!", "Sucess", JOptionPane.INFORMATION_MESSAGE);
                System.out.println("removing data");
                jcbEmpId.removeAllItems();
                System.out.println("Load emp id running");
                return;
                
        }
        JOptionPane.showMessageDialog(null,"Record not deleted!","Failure!",JOptionPane.ERROR_MESSAGE);
      }
      catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }
      catch(NumberFormatException ex){
                JOptionPane.showMessageDialog(null,"Please input numeric value for salary","Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            
        
        }
        }
    }//GEN-LAST:event_btnRemoveActionPerformed

    private void jcbEmpIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbEmpIdActionPerformed
        try{
            System.out.println("Checking combobox");
            if(jcbEmpId.getSelectedIndex() == -1){
                loadEmpID();
            }
            String empId = jcbEmpId.getSelectedItem().toString();
            EmpPojo emp = EmpDao.getEmployeeDetails(empId);
            txtEmpName.setText(emp.getEmpName());
            txtEmpSal.setText(String.valueOf(emp.getEmpSal()));
            jcbDepartment.setSelectedItem(emp.getEmpDepartment());
        }
        catch(SQLException ex){
                JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
        }        
    }//GEN-LAST:event_jcbEmpIdActionPerformed

